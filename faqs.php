                                            What is Hyperbaric Oxygen Therapy?
                                        </button>
                                    </h2>
                                    <div id="collapse1" class="accordion-collapse collapse show"
                                        aria-labelledby="heading1" data-bs-parent="#accordion">
                                        <div class="accordion-body">
                                            <p>Hyperbaric Oxygen Therapy (HBOT) is a medical treatment in which the
                                                patient is entirely enclosed in a pressure chamber breathing 100% pure
                                                oxygen (O2) at greater than one-atmosphere pressure. Air contains nearly
                                                21% oxygen and approximately 78% nitrogen. In hyperbaric oxygen therapy
                                                (HBO), the oxygen percentage breathed by the patient is nearly or
                                                actually 100%, almost five times more than in air. The pressure of the
                                                oxygen breathed by the patient in a hyperbaric oxygen chamber is usually
                                                more than 1.5 times (and can be as much as 3 times) greater than
                                                atmospheric pressure. HBOT can deliver nearly 15 times as much oxygen as
                                                there is in the air at normal pressure.
                                            </p>
                                        </div>
