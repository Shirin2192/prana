                                <div class="accordion-item wow fadeInUp" data-wow-delay="0.2s">
                                    <h2 class="accordion-header" id="heading2">
                                        <button class="accordion-button collapsed" type="button"
                                            data-bs-toggle="collapse" data-bs-target="#collapse2" aria-expanded="false"
                                            aria-controls="collapse2">
                                            What are the physiological mechanisms of action caused by HBOT that make it
                                            work?

                                        </button>
                                    </h2>
                                    <div id="collapse2" class="accordion-collapse collapse" aria-labelledby="heading2"
                                        data-bs-parent="#accordion">
                                        <div class="accordion-body">
                                            <p class="mb-2">Hyperoxygenation: HBOT physically dissolves extra oxygen
                                                into the blood plasma, which is then delivered to the tissues. Breathing
                                                pure oxygen at two to three times normal pressure delivers 10-15 times
                                                as much physically dissolved oxygen to tissues. This can increase the
                                                tissue oxygen in compromised tissues to greater-than-normal values.
                                                Hyperoxygenation has been demonstrated to induce the formation of new
                                                capillaries in ischemic or poorly perfused wounds. Therefore, it is
                                                useful in the treatment of ischemic-based compromised wounds, flaps, and
                                                grafts. It is also helpful in some infections by allowing white cell
                                                (leukocytic) activity to resume function.
                                            </p>
                                            <p class="mb-2">Mechanical Effect of Increased Pressure: Any gas in the body
                                                will decrease in volume as the pressure on it increases. With a
                                                threefold increase in pressure, a bubble trapped in the body is reduced
                                                by two-thirds. Thus, reduction in gas volume resolves air embolism and
                                                decompression illness when the diagnosis to treatment is done in a
                                                timely manner.
                                            </p>
                                            <p class="mb-2">Vasoconstriction: High-pressure oxygen causes constriction
                                                of the blood vessels in normal tissues without creating hypoxia. It does
                                                not cause constriction in previously oxygen-deprived tissues. The
                                                vasoconstriction decreases edema which is helpful in the treatment of
                                                burns, crush injuries, compartment syndromes, and other acute traumatic
                                                ischemias. Even though the blood flow that contributes to edema is
                                                reduced, oxygen delivery to the tissues is maintained through the
                                                hyperoxygenation effect.
                                            </p>
                                            <p class="mb-2">Antimicrobial Activity: HBOT inhibits alpha toxin production
                                                as seen in anaerobic infections such as clostridium perfringens (gas
                                                gangrene). The most common cause of gas gangrene is clostridium
                                                perfringens; however, there are several gas-producing organisms (aerobic
                                                and anaerobic) that require surgical debridement initially. It also
                                                enhances the white cell killing activity which provides an excellent
                                                adjuvant to I.V. antibiotics and local wound care.
                                            </p>
                                            <p class="mb-2">Mass Action of Gases: The flooding of the body with any one
                                                gas tends to “wash out” others. This action occurs more rapidly under
                                                pressure than under ordinary conditions and makes HBOT an indicated
                                                treatment for decompression sickness.
                                            </p>
                                            <p class="mb-2">Reduction of Reperfusion Injury: Following an ischemic
                                                interval, indirect injury occurs, which is mediated by the inappropriate
                                                activation of leukocytes. HBOT prevents such activation. Adherence of
                                                white blood cells to capillary walls is markedly reduced, thus
                                                mitigating the “no-reflow” phenomenon. This is why HBOT therapy is
                                                indicated in carbon monoxide poisoning and is considered the treatment
                                                of choice.
                                            </p>
                                        </div>
